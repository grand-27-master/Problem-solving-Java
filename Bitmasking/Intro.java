public class Intro{
    public static void main(String... args) {
        // AND 
        System.out.print(0&1);

        // OR
        System.out.println(0|1);

        // XOR
        System.out.println(0^0);

        // LEFT SHIFT
        System.out.println(10<<1);

        // RIGHT SHIFT
        System.out.println(10>>1);
    }
}